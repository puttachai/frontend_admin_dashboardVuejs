          // ‡πÄ‡∏Å‡πá‡∏ö deBlimit ‡πÑ‡∏ß‡πâ‡πÉ‡∏ô localStorage ‡πÇ‡∏î‡∏¢‡πÉ‡∏ä‡πâ customer_code ‡πÄ‡∏õ‡πá‡∏ô key
